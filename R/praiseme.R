#' A function that praises the user
#'
#' \code{praiseme} returns praise incorporating a user-specified string.
#'
#' @param your_name A string that is your name.
#'
#' @return A string with the praise
#' @export
#'
#' @examples
#'
#' praiseme("John")
praiseme <- function (your_name){

    paste("Hey", your_name, "you rock!!!")
}
